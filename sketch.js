var rBack=0,gBack=0,bBack=0;
var randJack=0;
var jack=[];
var a=0,b=1,c=2,d=3,f=4;
function setup(){
    createCanvas(1500,700,WEBGL);
}
function preload(){
    jack[0]=loadModel("jack1.obj",true);
    jack[1]=loadModel("jack2.obj",true);
    jack[2]=loadModel("jack3.obj",true);
    jack[3]=loadModel("jack4.obj",true);
    jack[4]=loadModel("jack5.obj",true);
}
function draw(){
    orbitControl();
    genOriginalSet();
    checkMPos();
    fill(0);
}
function checkMPos(){
    if((((mouseX-width/2)>-125)&&((mouseX-width/2)<125)
    &&((mouseY-height/2)>-125)&&((mouseY-height/2)<125))){
        noFill();
        stroke("yellow");
        box(250,250,250,0,0);
    }
    if((((mouseX-width/2)>125)&&((mouseX-width/2)<375)
    &&((mouseY-height/2)>-125)&&((mouseY-height/2)<125))){
        noFill();
        stroke("yellow");
        push();
        translate(250,0,0);
        box(250,250,250,0,0);
        pop();
    }
    if((((mouseX-width/2)>375)&&((mouseX-width/2)<500)
    &&((mouseY-height/2)>-125)&&((mouseY-height/2)<125))){
        noFill();
        stroke("yellow");
        push();
        translate(500,0,0);
        box(250,250,250,0,0);
        pop(); 
    }
    if((((mouseX-width/2)>-375)&&((mouseX-width/2)<-125)
    &&((mouseY-height/2)>-125)&&((mouseY-height/2)<125))){
        noFill();
        stroke("yellow");
        push();
        translate(-250,0,0);
        box(250,250,250,0,0);
        pop();
    }
    if((((mouseX-width/2)>-500)&&((mouseX-width/2)<-375)
    &&((mouseY-height/2)>-125)&&((mouseY-height/2)<125))){
        noFill();
        stroke("yellow");
        push();
        translate(-500,0,0);
        box(250,250,250,0,0);
        pop(); 
    }
}
function mouseReleased(){
    rBack=random(45,75);
    gBack=random(0,7);
    bBack=random(0,5);
    if((((mouseX-width/2)>-125)&&((mouseX-width/2)<125)
    &&((mouseY-height/2)>-125)&&((mouseY-height/2)<125))){
        randJack=int(random(0,4));
        a=randJack;
    }
    if((((mouseX-width/2)>125)&&((mouseX-width/2)<375)
    &&((mouseY-height/2)>-125)&&((mouseY-height/2)<125))){
        randJack=int(random(0,4));
        b=randJack;
    }
    if((((mouseX-width/2)>375)&&((mouseX-width/2)<500)
    &&((mouseY-height/2)>-125)&&((mouseY-height/2)<125))){
        randJack=int(random(0,4));
        c=randJack;
    }
    if((((mouseX-width/2)>-375)&&((mouseX-width/2)<-125)
    &&((mouseY-height/2)>-125)&&((mouseY-height/2)<125))){
        randJack=int(random(0,4));
        d=randJack;
    }
    if((((mouseX-width/2)>-500)&&((mouseX-width/2)<-375)
    &&((mouseY-height/2)>-125)&&((mouseY-height/2)<125))){
        randJack=int(random(0,4));
        f=randJack;
    }
}
function genOriginalSet(){
    push();
    directionalLight(255,117,24,0,-10,-20);
    directionalLight(255,117,24,0,10,20);
    pointLight(255,255,255,0,0,0);
    background(rBack,gBack,bBack);
    rotate(3.14);
    noStroke();
    ambientMaterial(255,117,24);
    model(jack[a]);
    pop();
    push();
    translate(250,0,0);
    directionalLight(255,117,24,0,-10,-20);
    directionalLight(255,117,24,0,10,20);
    pointLight(255,255,255,0,0,0);
    rotate(3.14);
    noStroke();
    ambientMaterial(255,117,24);
    model(jack[b]);
    pop();
    push();
    translate(500,0,0);
    directionalLight(255,117,24,0,-10,-20);
    directionalLight(255,117,24,0,10,20);
    pointLight(255,255,255,0,0,0);
    rotate(3.14);
    noStroke();
    ambientMaterial(255,117,24);
    model(jack[c]);
    pop();
    push();
    translate(-250,0,0);
    directionalLight(255,117,24,0,-10,-20);
    directionalLight(255,117,24,0,10,20);
    pointLight(255,255,255,0,0,0);
    rotate(3.14);
    noStroke();
    ambientMaterial(255,117,24);
    model(jack[d]);
    pop();
    push();
    translate(-500,0,0);
    directionalLight(255,117,24,0,-10,-20);
    directionalLight(255,117,24,0,10,20);
    pointLight(255,255,255,0,0,0);
    rotate(3.14);
    noStroke();
    ambientMaterial(255,117,24);
    model(jack[f]);
    pop();

}
